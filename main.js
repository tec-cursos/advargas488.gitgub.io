var infowindow;
var map;

function initMap() {
    map = new google.maps.Map(document.getElementById("map"), {
        zoom: 17,
        center: { lat: 10.076540677680523, lng: -84.304940700531 },
    });
    infowindow = new google.maps.InfoWindow();
    map.data.addGeoJson(
        data,
    );
    map.data.setStyle({
        fillColor: 'green',
        strokeWeight: 1
    });
    map.data.addListener('click', function (event) {
        if (event.feature.getProperty('ncasa') != undefined){
            document.getElementById('ciudad').innerText = "";
            document.getElementById('info').innerText =
                "Numero de casa: " + event.feature.getProperty('ncasa');
        }
        else if (event.feature.getProperty('city') != undefined) {
            document.getElementById('ciudad').innerText =
                "Ciudad del edificio: " + event.feature.getProperty('city');
            document.getElementById('info').innerText =
                "Nombre: " + event.feature.getProperty('name');
        }
        else if (event.feature.getProperty('name') != undefined){
            document.getElementById('ciudad').innerText = ""
            document.getElementById('info').innerText =
                event.feature.getProperty('name');
        }
        else{
            document.getElementById('ciudad').innerText ="";
            document.getElementById('info').innerText = "";
        }
    });
    map.data.setStyle(function (feature) {
        const postes = {
            // set svg icon svg
            url: "https://cdn-icons-png.flaticon.com/512/649/649784.png",
            // this marker is 765.9 pixels wide by 666 pixels high.
            scaledSize: new google.maps.Size(30, 40),
            // The anchor for this image is the base (0, 12).
        };

        const hidrantes = {
            // set svg icon svg
            url: "https://image.flaticon.com/icons/png/512/88/88318.png",
            // this marker is 765.9 pixels wide by 666 pixels high.
            scaledSize: new google.maps.Size(30, 40),
            // The anchor for this image is the base (0, 12).
        };

        const telefonos = {
            // set svg icon svg
            url: "https://encrypted-tbn0.gstatic.com/images?q=tbn:ANd9GcScEIN7DhLdjpeYbkO7xCt5PjRa_Ne1AVNy1l25zVr37C7aVQQ_HwANG2IsPhZmiuJZ8Tw&usqp=CAU",
            // this marker is 765.9 pixels wide by 666 pixels high.
            scaledSize: new google.maps.Size(30, 40),
            // The anchor for this image is the base (0, 12).
        };
        var titulo;
        var id = feature.getProperty('id');
        var img = 0;
        if(id == "posteluz"){
            img = postes;
            titulo = "Poste de luz"
        }
        else if(id == "hidrante"){
            img = hidrantes;
            titulo = "Hidrante"
        }
        else if(id == "telefonopublico"){
            img = telefonos;
            titulo = "Telefono publico"
        }
        return {
            title: titulo,
            icon: img,
        };
    });
}
